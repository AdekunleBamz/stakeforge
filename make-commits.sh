# Time range: 2pm (14:00) to 10:38pm (22:38) = 518 minutes
# Need ~340 commits, so roughly 1.5 min between commits on average
start_hour=14
start_min=0
total_minutes=518

count=0
for msg in "${messages[@]}"; do
  # Random file
  file=${files[$RANDOM % ${#files[@]}]}
  
  # Random time offset with some variance
  offset=$((count * total_minutes / ${#messages[@]}))
  variance=$((RANDOM % 3))
  offset=$((offset + variance))
  
  hour=$((start_hour + (start_min + offset) / 60))
  min=$(((start_min + offset) % 60))
  sec=$((RANDOM % 60))
  
  # Format time
  timestamp=$(printf "2026-01-13T%02d:%02d:%02d" $hour $min $sec)
  
  # Add content
  echo "/* ${msg} - $(date +%s) */" >> "$file"
  
  # Commit with timestamp
  git add -A
  GIT_AUTHOR_DATE="$timestamp" GIT_COMMITTER_DATE="$timestamp" git commit -m "$msg" 2>/dev/null
  
  count=$((count + 1))
  echo "[$count/${#messages[@]}] $msg"
done

echo ""
echo "Done! Total commits: $count"
echo "Pushing to GitHub..."
git push origin main
